package class6;

public class WhileLoop11 {
    public static void main(String[] args) {

        int number = 1;
        int sum=0;
        while(number<=10){
            sum=sum+number; // 1 , 3 , 6 , 10 , 15 , 21 , 28 , 36 , 45 , 55
            number++;
        }
        System.out.println(sum);











    }
}
